export type DiaryConsent = {
  /* имя поля формы = идентификатор согласия для лога согласия (PDN-SPEC §1.3) */
  name: string;
  /* текст согласия дословно; для CB со ссылкой содержит link.label как подстроку */
  text: string;
  /* встроенная ссылка на политику (CB1 → /privacy); null у CB без ссылки */
  link: { label: string; href: string } | null;
};

export type DiaryLetter = { n: number; when: string; title: string; text: string };

